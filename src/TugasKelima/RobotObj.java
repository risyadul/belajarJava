package TugasKelima;

public class RobotObj {
	private int x, y, fuel;
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getX() {
		return this.x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public int getY() {
		return this.y;
	}
	
	public void setFuel(int fuel) {
		this.fuel = fuel;
	}
	
	public int getFuel() {
		return this.fuel;
	}
}
